// for

for (let index = 0; index < 10; index++) {
  console.log(index);
}

/**
 * Ejemplo con for.
 * Dado un texto deseamos imprimir su contenido letra por letra
 */

let text = "Ciao Mondo!!";

for (let i = 0; i < text.length; i++) {
  console.log(text[i]);
}

/**
 * while
 */
let valor = 0;
while (valor < 10) {
    console.log(valor);
    valor++
}